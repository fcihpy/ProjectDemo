// Returns a copy of this image that is cropped to the given bounds.
// The bounds will be adjusted using CGRectIntegral.
// This method ignores the image's imageOrientation setting.
- (UIImage *)croppedImage:(CGRect)bounds {
    CGImageRef imageRef = CGImageCreateWithImageInRect([self CGImage], bounds);
    UIImage *croppedImage = [UIImage imageWithCGImage:imageRef];
    CGImageRelease(imageRef);
    return croppedImage;
}

#define mainBunbldSourcePath [[NSBundle mainBundle] resourcePath]
+(id) imageLoadFile:(NSString *)name{

    if ([name hasSuffix:@".png"] || [name hasSuffix:@".PNG"]) {
        name = [name substringToIndex:name.length - 4];
    }
    NSString *imageName = [name copy];
    UIImage * (^Image2XBlock)(NSString *) = ^UIImage *(NSString *fileName){
        NSString *thumbnailFile = [NSString stringWithFormat:@"%@/%@@2x.png", [[NSBundle mainBundle] resourcePath], fileName];
        UIImage *img = [UIImage imageWithContentsOfFile:thumbnailFile];
        return img;
    };
    UIImage * (^Image3XBlock)(NSString *) = ^UIImage *(NSString *fileName){
        NSString *thumbnailFile = [NSString stringWithFormat:@"%@/%@@3x.png", [[NSBundle mainBundle] resourcePath], fileName];
        UIImage *img = [UIImage imageWithContentsOfFile:thumbnailFile];
        return img;
    };

    if (([[UIDevice currentDevice] userInterfaceIdiom] == UIUserInterfaceIdiomPad)) {
        NSString *filePath = [mainBunbldSourcePath stringByAppendingPathComponent:[imageName stringByAppendingString:@"_ipad.png"]];
        UIImage *img = [UIImage imageWithContentsOfFile:filePath];
        if (!img) {
            filePath = [mainBunbldSourcePath stringByAppendingPathComponent:[imageName stringByAppendingString:@"@2x.png"]];
            return [UIImage imageWithContentsOfFile:filePath];
        }
        return img;
    }else{
        CGSize size = [[UIScreen mainScreen] bounds].size;
        if (size.width > 640.f) {
            
            UIImage *img3x = Image3XBlock(imageName);
            if (nil == img3x) {
                return Image2XBlock(imageName);
            }
            
        }else{
            return Image2XBlock(imageName);
        }
    }
    return nil;
    
}
+(id) imageColor:(UIColor *)color size:(CGSize)size{
    CGRect rect=CGRectMake(0.0f, 0.0f, size.width, size.height);
    UIGraphicsBeginImageContext(rect.size);
    CGContextRef context = UIGraphicsGetCurrentContext();
    CGContextSetFillColorWithColor(context, [color CGColor]);
    CGContextFillRect(context, rect);
    UIImage *theImage = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    return theImage;
}

